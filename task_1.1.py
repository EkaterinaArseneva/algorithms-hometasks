"""Выполнить логические побитовые операции "И", "ИЛИ" и др. над числами 5 и 6.
Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.
"""
print(f'"bitwise and" for 5 and 6 is: {5&6}')
print(f'"bitwise or" for 5 and 6 is: {5|6}')
print(f'"bitwise exclusive or" for 5 and 6 is: {5^6}')
print(f'"bitwise inversion" for 5 and 6 is: {~5}, {~6}')
print(f'"bitwise shift right" for 5 by 2 places is: {5>>2}')
print(f'"bitwise shift left" for 5 by 2 places is: {5<<2}')



